deduped_aliases AS (
    SELECT DISTINCT ON (canonical_name, LOWER(alias))
        canonical_name,
        alias,
        language,
        alias_type,
        weight,
        is_exact
    FROM seed_aliases
    ORDER BY
        canonical_name,
        LOWER(alias),
        CASE WHEN alias = canonical_name THEN 0 ELSE 1 END,
        weight DESC,
        is_exact DESC,
        alias
)
INSERT INTO public.entity_alias (
    entity_id,
    alias,
    language,
    alias_type,
    weight,
    is_exact,
    is_active,
    updated_at
)
SELECT
    e.entity_id,
    a.alias,
    a.language,
    a.alias_type,
    a.weight,
    a.is_exact,
    TRUE,
    NOW()
FROM seed_aliases a
JOIN all_entities e ON e.canonical_name = a.canonical_name
WHERE NOT EXISTS (
    SELECT 1
    FROM public.entity_alias existing
    WHERE existing.entity_id = e.entity_id
      AND LOWER(existing.alias) = LOWER(a.alias)
)
ON CONFLICT (entity_id, LOWER(alias)) DO NOTHING;
