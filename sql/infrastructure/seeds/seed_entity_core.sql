        wikidata_id,
        source,
        confidence,
        is_active,
        updated_at
    )
    SELECT canonical_name, entity_type, wikidata_id, source, confidence, TRUE, NOW()
    FROM seed_entities
    ON CONFLICT (canonical_name) DO UPDATE
    SET entity_type = EXCLUDED.entity_type,
        wikidata_id = COALESCE(EXCLUDED.wikidata_id, public.entity_registry.wikidata_id),
        source = EXCLUDED.source,
